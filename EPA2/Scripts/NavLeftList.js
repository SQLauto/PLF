function getStr(mySubItem, sType, pValue) {
    var rStr = ""
    if (typeof(mySubItem) == 'undefined') {       
                 rStr = pValue;
     }
    else {
        switch (sType) {
            case "ImgStr":
                rStr = '  <img src="images/submenu.png" /> ';
                break;
            case "Href":
                rStr = '#';
                break;
            case "Target":
                rStr = '_self';
                break;
            default:
                rStr = '_self';
                break;
        }
    }
    return rStr;
}

